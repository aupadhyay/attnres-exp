def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--threshold", type=float, default=0.01,
                        help="Mean absolute pixel diff threshold to flag as changed (0-1 scale)")
    parser.add_argument("--only", help="Only validate this figure name (substring match)")
    args = parser.parse_args()

    results = []
    tmpdir = tempfile.mkdtemp(prefix="attnres_validate_")
    print(f"Temp output dir: {tmpdir}\n")

    # Track which commands have already been run (for shared commands)
    ran_cmds = {}

    for fig in FIGURES:
        name = fig["name"]
        if args.only and args.only not in name:
            continue

        blog_path = os.path.join(BLOG_DIR, name)

        if not os.path.exists(blog_path):
            results.append((name, "MISSING_BLOG", 0.0, "Blog file not found"))
            continue

        if fig.get("skip"):
            results.append((name, "SKIPPED", 0.0, fig.get("reason", "")))
            continue

        if fig.get("known_bad"):
            results.append((name, "KNOWN_BAD", 0.0, fig.get("reason", "")))
            continue

        cmd_template = fig.get("cmd")
        if not cmd_template:
            results.append((name, "NO_CMD", 0.0, "No regeneration command defined"))
            continue

        # Handle commands that produce multiple output files (routing scripts)
        shared_with = fig.get("shared_cmd_with")
        output_in = fig.get("output_in")

        absolute_output = fig.get("absolute_output")

        if shared_with and shared_with in ran_cmds:
            # Reuse output dir from prior run
            out_path = os.path.join(ran_cmds[shared_with], output_in)
        else:
            fig_tmpdir = os.path.join(tmpdir, name.replace(".png", ""))
            os.makedirs(fig_tmpdir, exist_ok=True)
            out_path = absolute_output or os.path.join(fig_tmpdir, "out.png")

            # Build command with actual paths
            cmd = cmd_template.format(out=out_path, outdir=fig_tmpdir)

            print(f"[{name}] Running: {cmd[:80]}...")
            ok, log = run_cmd(cmd, BASE)
            if not ok:
                results.append((name, "CMD_FAILED", 0.0, log[-300:]))
                continue

            # If output_in is set, the script saved to a specific filename
            if output_in:
                out_path = os.path.join(fig_tmpdir, output_in)
                ran_cmds[name] = fig_tmpdir  # save for shared commands

        if not os.path.exists(out_path):
            results.append((name, "OUTPUT_MISSING", 0.0, f"Expected at {out_path}"))
            continue

        diff = img_diff(blog_path, out_path)
        status = "OK" if diff <= args.threshold else "CHANGED"
        results.append((name, status, diff, out_path))

    # Print results table
    print("\n" + "=" * 80)
    print(f"{'Figure':<45} {'Status':<12} {'PixelDiff':>10}  Notes")
    print("=" * 80)
    for name, status, diff, note in results:
        flag = "✓" if status in ("OK", "SKIPPED", "KNOWN_BAD") else "✗"
        diff_str = f"{diff:.4f}" if diff > 0 else "—"
        note_str = note if status in ("CMD_FAILED", "OUTPUT_MISSING", "KNOWN_BAD", "SKIPPED", "MISSING_BLOG") else ""
        print(f"  {flag}  {name:<43} {status:<12} {diff_str:>10}  {note_str[:50]}")

    n_changed = sum(1 for _, s, _, _ in results if s == "CHANGED")
    n_bad = sum(1 for _, s, _, _ in results if s == "KNOWN_BAD")
    n_ok = sum(1 for _, s, _, _ in results if s == "OK")
    print("=" * 80)
    print(f"\n  {n_ok} OK  |  {n_changed} CHANGED  |  {n_bad} KNOWN BAD\n")

    if n_changed > 0:
        sys.exit(1)


if __name__ == "__main__":
    main()
