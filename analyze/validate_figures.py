def img_diff(path_a: str, path_b: str) -> float:
    """Mean absolute pixel diff (0-1 scale) between two images, resized to match."""
    a = np.array(Image.open(path_a).convert("RGB")).astype(np.float32) / 255.0
    b_img = Image.open(path_b).convert("RGB")
    a_img = Image.open(path_a).convert("RGB")
    if a_img.size != b_img.size:
        b_img = b_img.resize(a_img.size, Image.LANCZOS)
    b = np.array(b_img).astype(np.float32) / 255.0
    return float(np.abs(a - b).mean())


def run_cmd(cmd: str, cwd: str) -> tuple[bool, str]:
    """Run shell command. Returns (success, stderr)."""
    result = subprocess.run(
        cmd, shell=True, cwd=cwd, capture_output=True, text=True
    )
    return result.returncode == 0, result.stderr + result.stdout


